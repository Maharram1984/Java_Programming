package day26_NESTED_LOOPS;

public class Month {
    public static void main(String[] args) {

        for (int week = 1; week <= 4 ; week++) {

            System.out.print("\nWeek: "+week+ "--->");

            for (int day = 1; day <= 7; day++) {
                System.out.print("\tDay: "+day);
            }

        }















    }
}
