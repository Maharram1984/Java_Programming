package day19_StringMethods;

public class indexOfMethod2 {
    public static void main(String[] args) {

        String str = "tarmacadam";
                  //  0123456789

        System.out.println("first a is: " +str.indexOf("a"));   // index 1
        System.out.println("second a is: " +str.indexOf("ac"));  // index 4
        System.out.println("third a is: " +str.indexOf("ad"));   // index 6
        System.out.println("last a is: " +str.lastIndexOf("a"));// index 8







    }
}
