package TASK;

public class ShortHandOperators {
    public static void main(String[] args) {

        int a = 100;                     // Assigned the last variable which is 290
        a = 200;
        a = 300;
        a = 290;
        System.out.println(a);

        int b = 100;                     // Addition Assignment
        b += 200;
        System.out.println(b);

        int c = 200;                     // Subtraction Assignment
        c -= 180;
        System.out.println(c);

        int d = 100;                     // Multiplication Assignment
        d *= 5;
        System.out.println(d);

        int e = 300;                     // Division Assignment
        e /= 100;
        System.out.println(e);

        int f = 80;                     //Remainder/Modulus Assignment
        f %= 7;
        System.out.println(f);
                                            /*  Example:
                                                 80 % 7 = 11.42...
                                                 7 * 11 = 77
                                                 80 - 77 = 3     */





    }

}
