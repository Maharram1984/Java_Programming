package TASK;

public class IncrementDecrement {
    public static void main(String[] args) {

        int A = -100;
        int B = +100;
        boolean aIsNegative = A<0;
        boolean bIsPositive = B>0;
        System.out.println(aIsNegative);
        System.out.println(bIsPositive);

        int preIncriment = 100;
        int preDecrement = 100;
        System.out.println(++preIncriment);
        System.out.println(--preDecrement);

        int w = 100;
        System.out.println(--w);
        System.out.println(++w);
        System.out.println(--w);
        System.out.println(++w);

        int d = 100;
        System.out.println(d++);
        System.out.println(d);

        int f = 100;
        System.out.println(f--);
        System.out.println(f);
























    }



}
