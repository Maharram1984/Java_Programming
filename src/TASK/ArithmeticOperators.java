package TASK;
public class ArithmeticOperators {
public static void main(String[] args) {

    System.out.println(100+239);
    System.out.println(234+34+32);   // addition or int
    int addition = 100+200+299;
    System.out.println(addition);

    System.out.println(100-200);     // Subtraction or int
    System.out.println(150-137);
    int subtraction = 100-97;
    System.out.println(subtraction);

    System.out.println(8*8);         // Multiplication or int
    System.out.println(9*7);
    int multiplication = 10*5;
    System.out.println(multiplication);

    System.out.println(10/4.0);       // Division or int
    System.out.println(10.0/4);
    System.out.println(10/4);
    System.out.println(10/4d);
    System.out.println(10d/4);
    System.out.println(10/3.0);
    int division = 10/3;             //  --->>> Can not get exactly answer by using "int"
    System.out.println(division);

    System.out.println(10%3);
    System.out.println(20%6);       // Modulus
    System.out.println(100%4);
    System.out.println(1000%3);
    System.out.println(80%6);
    int modulus = 100%3;
    System.out.println(modulus);









    }












}
