package TASK;



public class CastingExperience {
    public static void main(String[] args) {

        short amount = 100;                   // Bigger to smaller -- needs(casting)
        byte amount1 =(byte)amount;
        System.out.println(amount);
        System.out.println(amount1);

        float amount3 = 100000;               // smaller to bigger
        double amount4 = amount3;
        System.out.println(amount3);
        System.out.println(amount4);

        double amount5 = 100.1;               // bigger to smaller -- needs(casting)
        int amount6 =(int) amount5;
        System.out.println(amount5);
        System.out.println(amount6);

        long amount7 = 1000;                  // bigger to smaller -- needs(casting)
        int amount8 = (int) amount7;
        System.out.println(amount7);
        System.out.println(amount8);
























    }















}
