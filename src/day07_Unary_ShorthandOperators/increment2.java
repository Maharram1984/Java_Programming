package day07_Unary_ShorthandOperators;

public class increment2 {
    public static void main(String[] args) {

        int age = 20;
        System.out.println(age);      // 20
        System.out.println(age++);    // 20 still because it is post increment
        System.out.println(age);      // 21

        System.out.println(age++);    // 21 still because it is post increment
        System.out.println(age);      // 22

        System.out.println(++age);    // 23 pre increment
        System.out.println(++age);    // 24 pre increment








    }


}
