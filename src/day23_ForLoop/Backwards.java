package day23_ForLoop;

public class Backwards {
    public static void main(String[] args) {


        for(int i = 20; i > 0; i--){
            System.out.print(i+" ");
        }
        System.out.println();

        for (char a = 'z'; a >= 'a'; a--){
            System.out.print(a+" ");
        }

    }
}
