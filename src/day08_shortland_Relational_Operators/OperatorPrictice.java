package day08_shortland_Relational_Operators;

public class OperatorPrictice {
    public static void main(String[] args) {

        int b = 10;
        b++;                              // post increment
        System.out.println(b);            // 11
        b--;
        System.out.println(b);            // 10
        System.out.println(b++);          // 10
        System.out.println(b++);          // 11
        System.out.println(b);            // 12


        
        
        
    }
}

