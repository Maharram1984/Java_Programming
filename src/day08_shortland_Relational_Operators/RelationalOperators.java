package day08_shortland_Relational_Operators;

public class RelationalOperators {
    public static void main(String[] args) {

        System.out.println(10>5);     //true
        System.out.println(10<5);     //false
        int a = 5;
        int b = 3;
        System.out.println(a>b);      //true
        boolean aLessThan = a<b;
        System.out.println(aLessThan); // false

        System.out.println(6 >= 6);   // true
        System.out.println(10 >= 10); // true
        System.out.println(5 >= 8);   // false
        System.out.println(10 == 10);   // true
        System.out.println(10 == 11);   // false

        System.out.println(4 != 5);      //true
        System.out.println(4 != 4);      //false
        System.out.println(5 != 5);      // false
        System.out.println(10 != 10);    // false

















    }
}
